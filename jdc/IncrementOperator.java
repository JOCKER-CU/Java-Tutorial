class IncrementOpeator {
	public static void main(String[] args) {
		
		int i = 10;
		int j = 4;
		int k = 0;

		k = ++i + j;

		System.out.println("Pre Increment of i value : "+ i);
		System.out.println("Post Incremet of i value : "+ i);
		System.out.println("K value : "+ k);
		System.out.println("I value : "+ i);

	}
}